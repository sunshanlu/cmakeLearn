#include "MathFunctions.h"
#include "mySqrt.h"

namespace MathFunctions
{
    int add(int a, int b)
    {
        return ::add(a, b);
    }

    int sub(int a, int b)
    {
        return ::sub(a, b);
    }

    int mul(int a, int b)
    {
        return ::mul(a, b);
    }

    int sqrt(int a)
    {
        return ::sqrt(a);
    }
}
